package edu.kit.cargame.menu.pause;

/**
 * The buttons of the PauseMenu screen.
 */
public enum PauseMenuButtonTypes {
    /**
     * The first button with the text "Continue".
     */
    CONTINUE,
    /**
     * The third button with the text "Mute" or "Unmute", depending on state.
     */
    MUTE,
    /**
     * The fourth button with the text "Exit game".
     */
    TO_MENU
}
