package edu.kit.cargame.io.view.gamescreen;

/**
 * All actions the game screen can take.
 */
public enum GameScreenAction {
    /**
     * Switch to the pause screen.
     */
    PAUSE,
    /**
     * Goto the high-score menu.
     */
    GOTO_HIGHSCORE,

    /**
     * No action.
     */
    NONE
}
